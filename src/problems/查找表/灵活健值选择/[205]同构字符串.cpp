class Solution {
 public:
  bool isIsomorphic(string word, string pattern) {
    std::unordered_map<char, char> w_p, p_w;
    if (word.size() != pattern.size()) {
      return false;
    }
    for (size_t i = 0; i < word.size(); ++i) {
      auto w = word[i];
      auto p = pattern[i];
      if (!w_p.count(w)) {
        w_p[w] = p;
      }
      if (!p_w.count(p)) {
        p_w[p] = w;
      }
      if (w_p[w] != p || p_w[p] != w) {
        return false;
      }
    }
    return true;
  }
};
