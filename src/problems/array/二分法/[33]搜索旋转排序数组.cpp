class Solution {
 public:
  int search(vector<int>& nums, int target) {
    int l = 0;
    int r = nums.size() - 1;
    // [l...r]
    // å¾ªç¯ç»“æŸå
    // 1.å¦‚æœæ²¡æœ‰å‘ç”Ÿæ—‹è½¬ï¼Œl æŒ‡å‘ 0
    // 2.å‘ç”Ÿæ—‹è½¬ï¼Œnums[l] ä¸ºå³ä¾§èµ·ç‚¹
    while (l < r) {
      int m = l + (r - l) / 2;
      if (nums[m] > nums[r]) {
        l = m + 1;
      } else
        r = m;
    }
    // é»˜è®¤ä»å·¦ç«¯åºåˆ— [0...l-1] æ‰¾
    int left = 0, right = l - 1;
    r = nums.size() - 1;
    // å¦‚æœæ²¡æœ‰å‘ç”Ÿæ—‹è½¬ æˆ–è€… target <= nums[r]
    if (l == 0 || target <= nums[r]) {
      // ä»æ•´ä¸ªåºåˆ— [l...r] å¯»æ‰¾ target
      // ä»å³ç«¯åºåˆ— [l...r] å¯»æ‰¾ target
      left = l, right = r;
    }
    while (left <= right) {
      int mid = left + (right - left) / 2;
      if (nums[mid] > target) {
        right = mid - 1;
      } else if (nums[mid] < target) {
        left = mid + 1;
      } else
        return mid;
    }
    return -1;
  }
};
